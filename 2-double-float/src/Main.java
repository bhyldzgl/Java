
public class Main {
    public static void main(String[] args) {
        // Double : 64 bit - 8 byte
        // Float : 32 bit - 4 byte
        
       
        
       double Number1 = 70.25;
       double Number2 = 60d;
       double Number3 = 80.2;
       
       float fNumber1 = 70.25f;
       float fNumber2 = 60f;
       float fNumber3 = 80.2f;
        System.out.println("Average: " + (Number1 + Number2 + Number3) / 3 );
        System.out.println("Average: " + (fNumber1 + fNumber2 + fNumber3) / 3 );
        
       
    }
}