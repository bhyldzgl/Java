public class Main {
    
    public static void main(String[] args) {
        
        
        // Char data type : It is used to display characters.
        // Bolean data type : It is used in conditional situations.
        
        char a = 'A';
        char b = '?';
        
        // char c = '23';  
        // This is incorrect because it can only take a single character.
       
        System.out.println(a);
        System.out.println(b);
        
        
        boolean x = true;
        boolean y = false;
        
        System.out.println(x);
        System.out.println(y);
       
        
    }
}
