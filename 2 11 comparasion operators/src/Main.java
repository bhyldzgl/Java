public class Main {
 
    public static void main(String[] args) {
         
           /*
             ==  --> is it equal?
             !=  --> isn't equal? 
             >   --> is it big ?
             >=  --> greater than or equal to
             <   --> is it small ?
             <=  --> less than or equal to 
               
           */
           
           System.out.println(3 == 3);
           System.out.println(3 == 4);
           System.out.println(3 != 3);
           System.out.println(2 > 1);
           System.out.println(2 < 1);
           
    }
    
}
 